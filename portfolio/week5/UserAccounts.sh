#!/bin/bash

echo "Contents of /etc/passwd with /bin/bash as default shell:"

awk 'BEGIN {
    FS=":";
    printf("| \033[34m%-10s\033[0m | \033[34m%-6s\033[0m | \033[34m%-6s\033[0m | \033[34m%-20s\033[0m | \033[34m%-15s\033[0m |\n", "Username", "UserID", "GroupID", "Home", "Shell");
    print "----------------------------------------------------------------------------------------------------";
}

$7 ~ /\/bin\/bash/ {
    printf("| \033[33m%-10s\033[0m | \033[35m%-6s\033[0m | \033[35m%-6s\033[0m | \033[35m%-20s\033[0m | \033[35m%-15s\033[0m |\n", $1, $3, $4, $6, $7);
}

END {
    print("----------------------------------------------------------------------------------------------------");
}' /etc/passwd
